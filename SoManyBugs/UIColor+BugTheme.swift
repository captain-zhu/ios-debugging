import UIKit

extension UIColor {
    class func lightBlueColor() -> UIColor { return UIColor(red: 0.0, green: 122.0/255.0, blue: 1.0, alpha: 1.0) }
    class func darkGreenColor() -> UIColor { return UIColor(red: 64.0/255.0, green: 128.0/255.0, blue: 0.0, alpha: 1.0) }
}